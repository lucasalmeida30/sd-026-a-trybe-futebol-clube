// OtherModel.belongsTo(Example, { foreignKey: 'campoB', as: 'campoEstrangeiroB' });
Matche.belongsTo(Team, {
  foreignKey: 'homeTeamId',
  as: 'homeTeam',
});

Matche.belongsTo(Team, {
  foreignKey: 'awayTeamId',
  as: 'awayTeam',
});

